class MLSignalActorFacade(BaseMLInferenceActor):
    """
    Facade integrating all 5 signal actor components.

    This facade orchestrates:
    - SignalStrategyComponent: Signal generation strategies
    - PredictionBufferComponent: Ring buffer prediction/confidence history
    - AdaptiveThresholdComponent: Threshold calculation and regime detection
    - PerformanceMonitoringComponent: Performance metrics and timing
    - ModelWarmUpComponent: Model loading, warm-up, and hot reload

    Inherits 4-store integration from BaseMLInferenceActor:
    - FeatureStore, ModelStore, StrategyStore, DataStore

    Example:
        >>> from ml.actors import MLSignalActorConfig
        >>> config = MLSignalActorConfig(
        ...     model_path="model.onnx",
        ...     prediction_threshold=0.75,
        ...     signal_strategy="adaptive",
        ... )
        >>> actor = MLSignalActorFacade(config)
        >>> # Actor automatically initializes all 5 components and 4 stores

    """

    def __init__(self, config: MLSignalActorConfig) -> None:
        """
        Initialize facade with all 5 components.

        This method:
        1. Initializes base class (4 stores + 4 registries)
        2. Extracts configuration objects
        3. Creates all 5 components with proper dependencies
        4. Initializes strategy via component
        5. Sets up performance metrics

        Parameters
        ----------
        config : MLSignalActorConfig
            Complete configuration for the signal actor including:
            - Model path and inference settings
            - Signal generation strategy selection
            - Optimization configuration
            - Strategy configuration for adaptive thresholds

        Raises
        ------
        ValueError
            If required configuration is missing or invalid.

        """
        # 1. Initialize base class FIRST (4 stores + 4 registries)
        super().__init__(config)

        # Store signal-specific config reference
        self._signal_config = config
        self._persist_features: bool = bool(config.persist_features)

        # 2. Runtime imports (avoid circular dependencies at module level)
        from ml.actors.common.adaptive_threshold import AdaptiveThresholdComponent
        from ml.actors.common.model_warmup import ModelWarmUpComponent
        from ml.actors.common.performance_monitoring import PerformanceMonitoringComponent
        from ml.actors.common.prediction_buffer import PredictionBufferComponent
        from ml.actors.common.signal_strategy import SignalStrategyComponent
        from ml.config.actors import OptimizationConfig
        from ml.config.actors import StrategyConfig
        from ml.features import FeatureConfig
        from ml.features import FeatureEngineer

        # 3. Extract config objects with defaults
        opt_config = config.optimization_config or OptimizationConfig()
        strat_config = config.strategy_config or StrategyConfig(
            ensemble_weights={
                "threshold": 0.4,
                "extremes": 0.3,
                "momentum": 0.3,
            },
        )

        # Handle ensemble weights default
        if strat_config.ensemble_weights is None:
            strat_config = StrategyConfig(
                extremes_top_pct=strat_config.extremes_top_pct,
                momentum_lookback=strat_config.momentum_lookback,
                ensemble_weights={
                    "threshold": 0.4,
                    "extremes": 0.3,
                    "momentum": 0.3,
                },
                adaptive_volatility_factor=strat_config.adaptive_volatility_factor,
                min_threshold=strat_config.min_threshold,
                max_threshold=strat_config.max_threshold,
                update_frequency=strat_config.update_frequency,
            )

        # Determine if optimized mode
        try:
            is_optimized = getattr(opt_config.level, "value", str(opt_config.level)) == "optimized"
        except Exception:
            is_optimized = False

        # Feature configuration
        feature_config_raw = config.feature_config
        if feature_config_raw is None:
            feature_config = FeatureConfig()
        elif isinstance(feature_config_raw, FeatureConfig):
            feature_config = feature_config_raw
        else:
            feature_config = FeatureConfig()

        # 4. Initialize FeatureEngineer (same as legacy)
        self._feature_engineer = FeatureEngineer(feature_config)

        # Optional: align features to registry manifest for strict parity
        self._registry_feature_calculator: TFTRealtimeFeatureCalculator | None = None
        self._active_feature_names: list[str] = []
        self._feature_set_id = getattr(config, "feature_set_id", None)
        use_registry_features = bool(getattr(config, "use_registry_features", False))
        if use_registry_features:
            if not self._feature_set_id:
                raise ValueError(
                    "feature_set_id is required when use_registry_features=True",
                )
            try:
                manifest = self._feature_registry.get_feature_manifest(self._feature_set_id)
            except Exception as exc:
                self.log.exception(
                    "Failed to load FeatureManifest for registry features",
                    exc,
                )
                raise
            if manifest is None:
                raise ValueError(
                    f"FeatureManifest not found for feature_set_id={self._feature_set_id}",
                )
            from ml.features.common.tft_realtime_feature_calculator import (
                TFTRealtimeFeatureCalculator,
            )

            self._registry_feature_calculator = TFTRealtimeFeatureCalculator(
                feature_names=list(manifest.feature_names),
            )
            self._active_feature_names = list(manifest.feature_names)
            n_features = len(self._active_feature_names)
        else:
            n_features = self._feature_engineer.n_features
            try:
                self._active_feature_names = self._feature_engineer.get_feature_names()
            except Exception:
                self._active_feature_names = []

        # Pre-allocate feature buffer (hot path optimization)
        self._feature_buffer = np.zeros(n_features, dtype=np.float32)
        self._predict_input_buf = np.zeros((1, n_features), dtype=np.float32)

        # Store optimization config for later use
        self._opt_config = opt_config
        self._strat_config = strat_config
        self._feature_config_internal = feature_config

        # 5. Create all 5 components
        actor_id_str = str(self.id) if self.id else "unknown"

        # Component 1: SignalStrategyComponent
        self._signal_strategy_component: SignalStrategyComponent = SignalStrategyComponent(
            config=config,
            actor_id=actor_id_str,
            log=self.log,
        )

        # Component 2: PredictionBufferComponent
        self._prediction_buffer_component: PredictionBufferComponent = PredictionBufferComponent(
            capacity=config.adaptive_window,
            enable_history=(not is_optimized),
            actor_id=actor_id_str,
            log=self.log,
        )

        # Component 3: AdaptiveThresholdComponent
        base_threshold = float(config.prediction_threshold)
        min_threshold = float(strat_config.min_threshold)
        max_threshold = float(strat_config.max_threshold)
        if base_threshold < min_threshold or base_threshold > max_threshold:
            base_threshold = max(min_threshold, min(base_threshold, max_threshold))
            log_best_effort(
                self.log,
                "debug",
                "ml_actor.adaptive_threshold_clamped",
                extra={
                    "actor_id": actor_id_str,
                    "prediction_threshold": config.prediction_threshold,
                    "clamped_threshold": base_threshold,
                    "min_threshold": min_threshold,
                    "max_threshold": max_threshold,
                },
            )
        self._adaptive_threshold_component: AdaptiveThresholdComponent = AdaptiveThresholdComponent(
            base_threshold=base_threshold,
            volatility_factor=strat_config.adaptive_volatility_factor,
            min_threshold=strat_config.min_threshold,
            max_threshold=strat_config.max_threshold,
            actor_id=actor_id_str,
            log=self.log,
        )

        # Component 4: PerformanceMonitoringComponent
        reservoir_size = opt_config.reservoir_sample_size if is_optimized else 100
        self._performance_monitoring_component: PerformanceMonitoringComponent = (
            PerformanceMonitoringComponent(
                reservoir_size=reservoir_size,
                actor_id=actor_id_str,
                log=self.log,
            )
        )

        # Optional overrides for legacy-style tests (setters below).
        self._prediction_history_override: list[float] | None = None
        self._confidence_history_override: list[float] | None = None
        self._adaptive_threshold_override: float | None = None
        self._market_regime_override: str | None = None
        self._window_index_override: int | None = None
        self._window_count_override: int | None = None
        self._volatility_window_override: npt.NDArray[np.float32] | None = None

        # Component 5: ModelWarmUpComponent (conditionally initialized)
        # Only initialize if model_path is provided and exists
        self._model_warmup_component: ModelWarmUpComponent | None = None
        if config.model_path and Path(config.model_path).exists():
            try:
                self._model_warmup_component = ModelWarmUpComponent(
                    model_path=config.model_path,
                    n_features=n_features,
                    onnx_runtime_config=getattr(config, "onnx_runtime_config", None),
                    warm_up_iterations=10,
                    enable_parity_check=getattr(config, "enable_parity_smoke_check", False),
                    parity_tolerance=getattr(config, "parity_tolerance", 1e-6),
                    parity_window=getattr(config, "parity_smoke_check_window_bars", 200),
                    enable_hot_reload=config.enable_hot_reload,
                    hot_reload_interval=getattr(config, "hot_reload_interval", 300.0),
                    actor_id=actor_id_str,
                    log=self.log,
                )
            except (FileNotFoundError, ImportError) as exc:
                # Model warm-up is optional; log and continue
                self.log.exception(f"ModelWarmUpComponent not initialized: {exc}", exc)

        # 6. Initialize strategy via component
        self._signal_strategy: SignalGenerationStrategy = (
            self._signal_strategy_component.create_strategy(config=config)
        )

        # 7. Signal generation state (for legacy compatibility)
        self._last_signal_bar: int = -config.min_signal_separation_bars
        self._last_close_price: float | None = None
        self._prediction_count: int = 0
        self._last_feature_time_ns: int = 0

        # Parity smoke-check state (optional, delegated to Component 5)
        self._parity_enabled: bool = bool(getattr(config, "enable_parity_smoke_check", False))
        self._parity_window: int = int(getattr(config, "parity_smoke_check_window_bars", 200))
        self._parity_tolerance: float = float(getattr(config, "parity_tolerance", 1e-6))
        self._recent_bars: deque[Bar] = deque(maxlen=self._parity_window)
        self._recent_features: deque[npt.NDArray[np.float32]] = deque(maxlen=self._parity_window)
        self._parity_checked: bool = False

        # Hot reload state
        self._last_model_check: float = 0.0
        self._model_mtime: float | None = None

        # Indicator manager (initialized lazily in _compute_features)
        self._indicator_manager: IndicatorManager | None = None

        # 8. Initialize performance metrics (module-level)
        self._performance_monitoring_component.initialize_metrics()

        # 9. Optional actor-side domain event bridge (non-blocking)
        from ml.actors.ml_domain_events import init_actor_bus_bridge

        self._actor_bus_bridge, self._topic_scheme, self._topic_prefix = init_actor_bus_bridge(self)

        # Log initialization
        strategy_name_raw = config.signal_strategy
        from enum import Enum

        strategy_name_str: str = (
            strategy_name_raw.value
            if isinstance(strategy_name_raw, Enum)
            else str(strategy_name_raw)
        )

        self.log.info(
            f"MLSignalActorFacade initialized with strategy: {strategy_name_str}, "
            f"optimization: {opt_config.level}, features: {n_features}, "
            f"components: 5/5 initialized",
        )

    # =========================================================================
    # Hot Path Methods (P99 <5ms)
    # =========================================================================

    def _generate_prediction_protected(
        self,
        bar: Bar,
        features: npt.NDArray[np.float32],
    ) -> None:
        """
        Orchestrate prediction and signal generation pipeline.

        Order of operations (critical for parity):
        1. Check hot reload (cold path, infrequent)
        2. Generate prediction (base class)
        3. Update buffer (hot path)
        4. Detect regime (warm path)
        5. Generate signal (hot path)
        6. Record performance (warm path)
        7. Update parity bookkeeping (cold path, conditional)

        Parameters
        ----------
        bar : Bar
            Current price bar with OHLCV data.
        features : npt.NDArray[np.float32]
            Feature array from FeatureEngineer.

        Notes
        -----
        Hot path: This method is called on every bar after warm-up.
        Performance target: P99 <5ms end-to-end.

        """
        start_time = time.perf_counter()

        try:
            # 1. Hot reload check (Component 5 - cold path, infrequent)
            if self._should_hot_reload():
                self._execute_hot_reload()

            # 2. Generate prediction (or force in test mode)
            if os.getenv("FORCE_SIGNAL_MODE", "false").lower() == "true":
                prediction, confidence = 1.0, 1.0
            else:
                prediction, confidence = self._predict(features)

            self._prediction_count += 1

            # 3. Update buffer (Component 2 - hot path)
            volatility = self._calculate_volatility(bar)
            self._prediction_buffer_component.update(prediction, confidence, volatility)

            # 4. Detect regime (Component 3 - warm path)
            # Use property accessors for buffer metadata
            volatility_window = self._prediction_buffer_component.volatility_window
            count = self._prediction_buffer_component.window_count
            self._adaptive_threshold_component.detect_regime(volatility_window, count)

            # Persist prediction to model store (same as legacy)
            self._persist_prediction(bar, features, prediction, confidence, start_time)

            # 5. Generate signal (hot path)
            self._try_generate_signal(bar, prediction, confidence, features)

            # 6. Record performance (Component 4 - warm path)
            total_time_ns = int((time.perf_counter() - start_time) * 1_000_000_000)
            feature_time_ns = self._last_feature_time_ns
            inference_time_ns = max(0, total_time_ns - feature_time_ns)
            self._performance_monitoring_component.record_timing(
                feature_time_ns=feature_time_ns,
                inference_time_ns=inference_time_ns,
                total_time_ns=total_time_ns,
            )

            # 7. Update health monitor (base class)
            self._record_success()

            # 8. Parity bookkeeping (Component 5 - cold path, conditional)
            if self._parity_enabled:
                self._recent_bars.append(bar)
                self._recent_features.append(features.copy())
                if not self._parity_checked and len(self._recent_bars) >= self._parity_window:
                    self._run_parity_smoke_check()

        except Exception as e:
            self.log.exception(f"Error in prediction pipeline: {e}", e)
            self._performance_monitoring_component.record_error()
            self._record_failure()

    def _try_generate_signal(
        self,
        bar: Bar,
        prediction: float,
        confidence: float,
        features: npt.NDArray[np.float32],
    ) -> None:
        """
        Execute signal generation pipeline.

        Order of operations:
        1. Apply pending strategy swap (Component 1)
        2. Check signal separation constraint
        3. Build context for strategy (Component 2 + Component 3)
        4. Generate signal (Component 1)
        5. Publish and persist signal
        6. Record metrics (Component 4)

        Parameters
        ----------
        bar : Bar
            Current price bar.
        prediction : float
            Model prediction [-1, 1].
        confidence : float
            Prediction confidence [0, 1].
        features : npt.NDArray[np.float32]
            Feature array.

        Notes
        -----
        Hot path: Called on every prediction above threshold.
        Performance target: P99 <100us for signal generation.

        """
        try:
            # 1. Apply pending strategy swap (Component 1 - O(1))
            strategy_component = getattr(self, "_signal_strategy_component", None)
            if strategy_component is not None:
                strategy_component.apply_pending_swap()

            # 2. Check signal separation
            bars_since_last = self._bars_processed - self._last_signal_bar
            if bars_since_last < self._signal_config.min_signal_separation_bars:
                return

            # 3. Determine timestamp for context
            clock_obj = getattr(self, "clock", None)
            if clock_obj is None:
                timestamp_ns = bar.ts_init
            else:
                try:
                    timestamp_ns = int(clock_obj.timestamp_ns())
                except Exception:
                    timestamp_ns = bar.ts_init

            # 4. Build context (Component 2 + Component 3)
            buffer_component = getattr(self, "_prediction_buffer_component", None)
            if buffer_component is not None:
                ring_metadata = buffer_component.get_ring_metadata()
            else:
                ring_metadata = {
                    "_prediction_ring": getattr(self, "_prediction_window", None),
                    "_prediction_ring_index": getattr(self, "_window_index", 0),
                    "_prediction_ring_count": getattr(self, "_window_count", 0),
                }

            prediction_history_full = getattr(self, "_prediction_history", [])
            confidence_history_full = getattr(self, "_confidence_history", [])
            lookback = min(10, len(prediction_history_full))
            if lookback:
                prediction_history = list(prediction_history_full[-lookback:])
                confidence_history = (
                    list(confidence_history_full[-lookback:]) if confidence_history_full else []
                )
            else:
                prediction_history = []
                confidence_history = []

            threshold_component = getattr(self, "_adaptive_threshold_component", None)
            if threshold_component is not None:
                adaptive_threshold = threshold_component.current_threshold
                market_regime = threshold_component.current_regime
            else:
                adaptive_threshold = float(getattr(self, "_adaptive_threshold", 0.0))
                market_regime = getattr(self, "_market_regime", "unknown")

            # Build complete context for strategy
            context: dict[str, Any] = {
                "prediction_history": prediction_history,
                "confidence_history": confidence_history,
                "adaptive_threshold": adaptive_threshold,
                "market_regime": market_regime,
                "log_predictions": self._config.log_predictions,
                "timestamp_ns": timestamp_ns,
                "model_id": self._model_id if hasattr(self, "_model_id") else "unknown",
                # Ring buffer metadata for zero-copy strategy access
                "_prediction_ring": ring_metadata.get("_prediction_ring"),
                "_prediction_ring_index": ring_metadata.get("_prediction_ring_index", 0),
                "_prediction_ring_count": ring_metadata.get("_prediction_ring_count", 0),
            }

            # 5. Generate signal (Component 1 - hot path)
            strategy = getattr(self, "_signal_strategy", None)
            if strategy is None and strategy_component is not None:
                strategy = strategy_component.current_strategy
            if strategy is None:
                return
            signal = strategy.generate_signal(
                bar,
                prediction,
                confidence,
                features,
                context,
            )

            if signal is None:
                return

            # 6. Update last signal bar
            self._last_signal_bar = self._bars_processed

            # 7. Publish signal (base class)
            self._publish_signal(signal)

            # 8. Persist to strategy store (base class store)
            if hasattr(self, "_strategy_store") and self._strategy_store is not None:
                self._strategy_store.write_signal(
                    strategy_id=(str(self.id) if getattr(self, "id", None) else "ml_signal"),
                    instrument_id=str(bar.bar_type.instrument_id),
                    signal_type="buy" if signal.prediction > 0 else "sell",
                    strength=abs(signal.prediction),
                    model_predictions={context.get("model_id", "unknown"): prediction},
                    risk_metrics={"confidence": confidence},
                    execution_params={"threshold": adaptive_threshold},
                    ts_event=bar.ts_event,
                )

            # 9. Record metrics (Component 4)
            monitor = getattr(self, "_performance_monitoring_component", None) or getattr(
                self,
                "_performance_monitor",
                None,
            )
            if monitor is not None and hasattr(monitor, "record_signal"):
                monitor.record_signal()

            # 10. Emit Prometheus metrics
            actor_id_str = str(self.id) if self.id else "unknown"
            strategy_name_raw_2 = self._signal_config.signal_strategy
            from enum import Enum as _Enum

            strategy_name_str_2: str = (
                strategy_name_raw_2.value
                if isinstance(strategy_name_raw_2, _Enum)
                else str(strategy_name_raw_2)
            )

            metric = getattr(self, "_signals_generated_metric", None) or _signals_generated_metric
            if metric is not None:
                metric.labels(
                    actor_id=actor_id_str,
                    strategy=strategy_name_str_2,
                    signal_type="buy" if signal.prediction > 0 else "sell",
                ).inc()

        except Exception as exc:
            log = getattr(self, "log", None)
            if log is not None:
                log_best_effort(
                    log,
                    "error",
                    "ml_signal_actor.signal_generation_failed",
                    exc_info=True,
                    extra={"error": str(exc)},
                )
            monitor = getattr(self, "_performance_monitoring_component", None) or getattr(
                self,
                "_performance_monitor",
                None,
            )
            if monitor is not None and hasattr(monitor, "record_error"):
                monitor.record_error()

    def _publish_signal(self, signal: MLSignal) -> None:
        """
        Publish signal with optional actor-side domain event emission.
        """
        super()._publish_signal(signal)

        bridge = getattr(self, "_actor_bus_bridge", None)
        if bridge is None:
            return

        try:
            from ml.common.correlation import make_correlation_id
            from ml.common.message_topics import build_topic_for_stage
            from ml.config.events import EventStatus
            from ml.config.events import Source
            from ml.config.events import Stage

            instrument_str = str(signal.instrument_id)
            topic = build_topic_for_stage(
                Stage.SIGNAL_EMITTED,
                instrument_str,
                scheme=str(getattr(self, "_topic_scheme", "domain_op")),
                prefix=str(getattr(self, "_topic_prefix", "events.ml")),
            )

            cache_obj = getattr(self, "cache", None)
            is_backtesting = (
                bool(getattr(cache_obj, "is_backtesting", False)) if cache_obj else False
            )
            source = Source.HISTORICAL.value if is_backtesting else Source.LIVE.value
            ts_event = int(signal.ts_event)
            correlation_id = make_correlation_id(
                run_id=f"actor_{self.id}",
                dataset_id="signals",
                instrument_id=instrument_str,
                ts_min=ts_event,
                ts_max=ts_event,
                count=1,
            )

            payload = {
                "dataset_id": "signals",
                "instrument_id": instrument_str,
                "stage": Stage.SIGNAL_EMITTED.value,
                "status": EventStatus.SUCCESS.value,
                "source": source,
                "model_id": signal.model_id,
                "prediction": float(signal.prediction),
                "confidence": float(signal.confidence),
                "ts_event": ts_event,
                "metadata": {"correlation_id": correlation_id},
            }

            bridge.publish(topic, payload)
        except Exception as exc:
            log_best_effort(
                self.log,
                "debug",
                "ml_signal_actor.actor_bus_publish_failed",
                exc_info=True,
                extra={"error": str(exc)},
            )

