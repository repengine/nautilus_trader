    def _update_prediction_history(self, prediction: float, confidence: float, bar: Bar) -> None:
        """
        Update prediction history and volatility buffers (legacy compatibility).

        Parameters
        ----------
        prediction : float
            Model prediction.
        confidence : float
            Prediction confidence.
        bar : Bar
            Current price bar.

        """
        buffer_component = getattr(self, "_prediction_buffer_component", None)
        if buffer_component is None:
            return
        volatility = self._calculate_volatility(bar)
        buffer_component.update(prediction, confidence, volatility)
        threshold_component = getattr(self, "_adaptive_threshold_component", None)
        if threshold_component is not None:
            threshold_component.detect_regime(
                buffer_component.volatility_window,
                buffer_component.window_count,
            )

    def _detect_market_regime(self, _bar: Bar) -> str:
        """
        Detect market regime using buffered volatility (legacy compatibility).

        Parameters
        ----------
        _bar : Bar
            Current price bar (unused; retained for signature parity).

        """
        buffer_component = getattr(self, "_prediction_buffer_component", None)
        threshold_component = getattr(self, "_adaptive_threshold_component", None)
        if buffer_component is None or threshold_component is None:
            return "unknown"
        return cast(
            str,
            threshold_component.detect_regime(
                buffer_component.volatility_window,
                buffer_component.window_count,
            ),
        )

    def _calculate_volatility(self, bar: Bar) -> float:
        """
        Calculate price volatility for current bar.

        Parameters
        ----------
        bar : Bar
            Current price bar.

        Returns
        -------
        float
            Absolute price change from last close.

        """
        if self._last_close_price is None:
            self._last_close_price = float(bar.close.as_double())
            return 0.0

        volatility = abs(float(bar.close.as_double()) - self._last_close_price)
        self._last_close_price = float(bar.close.as_double())
        return volatility

    def _persist_prediction(
        self,
        bar: Bar,
        features: npt.NDArray[np.float32],
        prediction: float,
        confidence: float,
        start_time: float,
    ) -> None:
        """
        Persist prediction to model store (same as legacy).

        Parameters
        ----------
        bar : Bar
            Current price bar.
        features : npt.NDArray[np.float32]
            Feature array.
        prediction : float
            Model prediction.
        confidence : float
            Prediction confidence.
        start_time : float
            Start time of prediction pipeline (for latency calculation).

        """
        try:
            # Build feature dictionary with names if available
            names = list(self._active_feature_names)
            if not names:
                try:
                    names = list(
                        getattr(self._feature_engineer.config, "get_feature_names")(),
                    )
                except Exception as exc:
                    self.log.exception("Failed to fetch feature names; continuing", exc)
                    names = []

            if names and len(names) == features.shape[0]:
                feature_dict: dict[str, float] = {
                    names[i]: float(features[i]) for i in range(len(names))
                }
            else:
                feature_dict = {
                    f"feature_{i}": float(features[i]) for i in range(int(features.shape[0]))
                }

            # Calculate inference time
            inference_time_ms = (time.perf_counter() - start_time) * 1000.0

            # Write to model store
            self._model_store.write_prediction(
                model_id=self._model_id,
                instrument_id=str(bar.bar_type.instrument_id),
                prediction=float(prediction),
                confidence=float(confidence),
                features=feature_dict,
                inference_time_ms=inference_time_ms,
                ts_event=bar.ts_event,
                is_live=True,
            )
        except Exception as exc:
            # Non-fatal; continue to signal generation
            self.log.exception("Model inference context build failed; continuing", exc)

    def _should_hot_reload(self) -> bool:
        """
        Check if hot reload should be performed.

        Returns
        -------
        bool
            True if hot reload is enabled and it's time to check for updates.

        """
        if not self._config.enable_hot_reload:
            return False

        # Check if enough time has passed since last check
        current_time = time.time()
        hot_reload_interval = getattr(self._signal_config, "hot_reload_interval", 300.0)
        if current_time - self._last_model_check < float(hot_reload_interval):
            return False

        self._last_model_check = current_time
        return True

    def _execute_hot_reload(self) -> None:
        """
        Execute hot reload of the model if a new version is available.

        Delegates to Component 5 if available, otherwise uses legacy implementation.

        """
        try:
            # Prefer component 5 if available
            if self._model_warmup_component is not None:
                if self._model_warmup_component.should_hot_reload():
                    # execute_hot_reload requires model_path and load_model_fn
                    model_path = self._config.model_path or ""
                    result = self._model_warmup_component.execute_hot_reload(
                        model_path=model_path,
                        load_model_fn=lambda p: self._model_warmup_component.load_model(),  # type: ignore[union-attr]
                    )
                    if result is not None:
                        self._model, self._model_metadata = result
                return

            # Fallback to legacy implementation
            if not self._config.model_path or not Path(self._config.model_path).exists():
                return

            # Get current model modification time
            current_mtime = Path(self._config.model_path).stat().st_mtime
            if self._model_mtime is not None and current_mtime <= self._model_mtime:
                return

            # Load new model
            self.log.info(f"Hot reloading model from {self._config.model_path}")
            self._load_model_with_metadata()
            self._model_mtime = current_mtime

        except Exception as e:
            self.log.exception(f"Failed to hot reload model: {e}", e)

    def _run_parity_smoke_check(self) -> None:
        """
        Compute features offline over the recent window and compare to online results.

        Emits `ml_feature_parity_checks_total` and updates `ml_feature_parity_drift` gauge.

        """
        try:
            offline_vectors: list[npt.NDArray[np.float32]] = []
            for b in self._recent_bars:
                vec = self._compute_features(b)
                if vec is not None:
                    offline_vectors.append(vec.copy())

            n_online = len(self._recent_features)
            n_offline = len(offline_vectors)
            n = min(n_online, n_offline)
            if n == 0:
                return

            online = np.stack(list(self._recent_features)[-n:])
            offline = np.stack(offline_vectors[-n:])
            drift = float(np.max(np.abs(online - offline)))

            # Emit metrics
            actor_label = str(self.id) if self.id is not None else "unknown"
            parity_checks_counter = get_counter(
                "ml_feature_parity_checks_total",
                "Total feature parity checks performed",
                ["actor_id"],
            )
            parity_drift_gauge = get_gauge(
                "ml_feature_parity_drift",
                "Feature parity drift (max absolute difference)",
                ["actor_id"],
            )

            parity_checks_counter.labels(actor_id=actor_label).inc()
            parity_drift_gauge.labels(actor_id=actor_label).set(drift)

            if drift > self._parity_tolerance:
                self.log.warning(
                    f"Feature parity drift {drift:.3e} exceeded tolerance {self._parity_tolerance:.3e}",
                )
        finally:
            self._parity_checked = True

