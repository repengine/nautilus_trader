    def _compute_features(self, bar: Bar) -> npt.NDArray[np.float32] | None:
        """
        Compute feature vector from current bar data.

        This method is called in the hot path and must be optimized:
        - Use pre-allocated numpy arrays
        - Update indicators in-place
        - Return None if features are not ready

        Parameters
        ----------
        bar : Bar
            The current bar data.

        Returns
        -------
        npt.NDArray[np.float32] | None
            The computed feature vector, or None if not ready.

        """
        registry_calculator = self._registry_feature_calculator
        use_registry_features = registry_calculator is not None
        use_feature_store = bool(getattr(self._config, "use_feature_store", False))
        # Delegate to feature store only when explicitly enabled
        if not use_registry_features and use_feature_store:
            start_time = time.perf_counter()
            try:
                if (
                    hasattr(self, "_feature_store")
                    and self._feature_store is not None
                    and hasattr(self._feature_store, "compute_realtime")
                ):
                    compute = cast(Any, getattr(self._feature_store, "compute_realtime"))
                    features = cast(
                        npt.NDArray[np.float32] | None,
                        compute(bar=bar, store=getattr(self, "_persist_features", False)),
                    )
                    if isinstance(features, np.ndarray) and features.size == 0:
                        features = None
                    if features is not None:
                        feature_time_ms = (time.perf_counter() - start_time) * 1000.0
                        self._last_feature_time_ns = int(feature_time_ms * 1_000_000)
                        _record_feature_time_metric(self, feature_time_ms)
                        return features
            except Exception as exc:
                self.log.exception(
                    f"FeatureStore compute_realtime failed; falling back: {exc}",
                    exc,
                )

        # Fallback to FeatureEngineer
        from ml.features.indicators import IndicatorManager

        if not hasattr(self, "_indicator_manager") or self._indicator_manager is None:
            self._indicator_manager = IndicatorManager(self._feature_config_internal)

        start_time = time.perf_counter()
        self._indicator_manager.update_from_bar(bar)

        if use_registry_features:
            assert registry_calculator is not None
            required = registry_calculator.required_history_bars
            closes = self._indicator_manager.price_history.get("closes") or []
            if len(closes) < required:
                return None
            features_result = registry_calculator.compute(
                bar=bar,
                indicator_manager=self._indicator_manager,
            )
        else:
            if not self._indicator_manager.all_initialized():
                if self._bars_processed < self._config.warm_up_period:
                    return None

            current_bar = {
                "close": float(bar.close),
                "volume": float(bar.volume),
                "high": float(bar.high),
                "low": float(bar.low),
            }

            features_result = self._feature_engineer.calculate_features_online(
                current_bar=current_bar,
                indicator_manager=self._indicator_manager,
                scaler=None,
            )

        feature_time_ms = (time.perf_counter() - start_time) * 1000.0
        self._last_feature_time_ns = int(feature_time_ms * 1_000_000)
        _record_feature_time_metric(self, feature_time_ms)

        return features_result

    def _sanitize_prediction_output(
        self,
        prediction: Any,
        confidence: Any | None,
    ) -> tuple[float, float]:
        output_is_logits = resolve_output_is_logits(self._model_metadata)
        classes = getattr(self._model, "classes_", None)
        positive_class_index = resolve_positive_class_index(
            self._model_metadata,
            classes=classes,
            num_classes=len(classes) if classes is not None else None,
        )
        return normalize_prediction_output(
            prediction,
            confidence,
            positive_class_index=positive_class_index,
            output_is_logits=output_is_logits,
        )

    def _predict(self, features: npt.NDArray[np.float32]) -> tuple[float, float]:
        """
        Generate prediction from feature vector.

        Parameters
        ----------
        features : npt.NDArray[np.float32]
            The feature vector for prediction.

        Returns
        -------
        tuple[float, float]
            A tuple of (probability, confidence) values.

        """
        if self._model is None:
            return self._sanitize_prediction_output(0.5, 0.0)

        try:
            # Check for Mock objects (testing)
            from unittest.mock import MagicMock
            from unittest.mock import Mock

            if isinstance(self._model, Mock | MagicMock):
                # Handle mock objects for testing
                if hasattr(self._model, "predict_proba"):
                    size = features.shape[0]
                    self._predict_input_buf[0, :size] = features
                    probabilities = self._model.predict_proba(self._predict_input_buf)[0]
                    return self._sanitize_prediction_output(probabilities, None)
                elif hasattr(self._model, "run"):
                    size = features.shape[0]
                    self._predict_input_buf[0, :size] = features
                    mock_input_name = self._model_metadata.get("input_names", ["input"])[0]
                    outputs = self._model.run(None, {mock_input_name: self._predict_input_buf})
                    if len(outputs) >= 2:
                        return self._sanitize_prediction_output(
                            outputs[0],
                            outputs[1],
                        )
                    return self._sanitize_prediction_output(
                        outputs[0],
                        None,
                    )
                elif hasattr(self._model, "predict"):
                    size = features.shape[0]
                    self._predict_input_buf[0, :size] = features
                    prediction = float(self._model.predict(self._predict_input_buf)[0])
                    return self._sanitize_prediction_output(prediction, None)

            # ONNX model
            if hasattr(self._model, "run"):
                input_name: str | None = None
                metadata_names = self._model_metadata.get("input_names")
                if isinstance(metadata_names, list) and metadata_names:
                    input_name = str(metadata_names[0])
                elif hasattr(self._model, "get_inputs"):
                    try:
                        input_name = str(self._model.get_inputs()[0].name)
                        if input_name:
                            self._model_metadata.setdefault("input_names", [input_name])
                    except Exception:
                        input_name = None

                if input_name:
                    features_2d = self._prepare_onnx_input(features)
                    outputs = self._model.run(None, {input_name: features_2d})

                    if len(outputs) >= 2:
                        return self._sanitize_prediction_output(
                            outputs[0],
                            outputs[1],
                        )
                    return self._sanitize_prediction_output(
                        outputs[0],
                        None,
                    )

            # Scikit-learn with predict_proba
            if hasattr(self._model, "predict_proba"):
                features_2d = features.reshape(1, -1)
                probabilities = self._model.predict_proba(features_2d)[0]
                return self._sanitize_prediction_output(probabilities, None)

            # Generic predict
            if hasattr(self._model, "predict"):
                features_2d = features.reshape(1, -1)
                prediction = float(self._model.predict(features_2d)[0])
                return self._sanitize_prediction_output(prediction, None)

            self.log.error(f"Unsupported model type: {type(self._model)}")
            return self._sanitize_prediction_output(0.5, 0.0)

        except Exception as e:
            self.log.exception(f"Prediction failed: {e}", e)
            raise

    def _prepare_onnx_input(
        self,
        features: npt.NDArray[np.float32],
    ) -> npt.NDArray[np.float32]:
        """
        Align features to ONNX model input shape with a guarded fallback.

        Returns a 2D float32 array sized to the model input dimension. If the input
        dimension cannot be resolved, falls back to reshaping the provided features.
        """
        features_array = np.asarray(features, dtype=np.float32).reshape(-1)
        expected_dim = self._resolve_model_input_dim()
        if expected_dim <= 0:
            return features_array.reshape(1, -1)

        if self._predict_input_buf.shape[1] != expected_dim:
            self._predict_input_buf = np.zeros((1, expected_dim), dtype=np.float32)

        if features_array.size != expected_dim:
            if not self._input_dim_mismatch_logged:
                self.log.warning(
                    "MLSignalActor inference input dim mismatch; aligning features "
                    f"actor={self.id} expected={expected_dim} got={features_array.size}",
                )
                self._input_dim_mismatch_logged = True
            _inference_fallback_counter.labels(
                component="signal_actor_inference",
                level="input_shape_mismatch",
            ).inc()
            self._predict_input_buf.fill(0.0)
            slice_len = min(features_array.size, expected_dim)
            if slice_len > 0:
                self._predict_input_buf[0, :slice_len] = features_array[:slice_len]
        else:
            self._predict_input_buf[0, :expected_dim] = features_array

        return self._predict_input_buf

    def _extract_output_scalar(self, output: Any, *, label: str) -> float:
        """
        Extract a scalar from model output with guarded fallbacks.
        """
        array = np.asarray(output)
        if array.size == 0:
            if not self._output_shape_mismatch_logged:
                self.log.warning(
                    "MLSignalActor inference output empty; using 0.0 "
                    f"actor={self.id} label={label}",
                )
                self._output_shape_mismatch_logged = True
            _inference_fallback_counter.labels(
                component="signal_actor_inference",
                level="output_empty",
            ).inc()
            return 0.0

        if array.size != 1:
            if not self._output_shape_mismatch_logged:
                self.log.warning(
                    "MLSignalActor inference output shape mismatch; taking first element "
                    f"actor={self.id} label={label} size={array.size}",
                )
                self._output_shape_mismatch_logged = True
            _inference_fallback_counter.labels(
                component="signal_actor_inference",
                level="output_shape_mismatch",
            ).inc()

        return float(array.reshape(-1)[0])

    def _resolve_model_input_dim(self) -> int:
        """
        Resolve the ONNX model input dimension from cached metadata or session.
        """
        input_shape = self._model_metadata.get("input_shape")
        if isinstance(input_shape, (list, tuple)) and len(input_shape) >= 2:
            dim = input_shape[1]
            try:
                dim_int = int(dim) if dim is not None else 0
            except (TypeError, ValueError):
                dim_int = 0
            if dim_int > 0:
                return dim_int

        if hasattr(self._model, "get_inputs"):
            try:
                shape = self._model.get_inputs()[0].shape
            except Exception:
                self.log.debug(
                    "MLSignalActor failed to resolve model input shape from session",
                    exc_info=True,
                )
                return 0
            if isinstance(shape, (list, tuple)) and len(shape) >= 2:
                dim = shape[1]
                try:
                    dim_int = int(dim) if dim is not None else 0
                except (TypeError, ValueError):
                    dim_int = 0
                if dim_int > 0:
                    return dim_int

        return 0


