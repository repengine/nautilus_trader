    def reset_signal_state(self) -> None:
        """
        Reset all component state.

        Delegates to:
        - Component 2: Reset buffer
        - Component 3: Reset threshold (via update with zero volatility)
        - Component 4: Reset is not available (counters not resettable in component)

        This is a cold path operation with allocations allowed.

        """
        # Component 2: Reset buffer
        self._prediction_buffer_component.reset()

        # Component 3: Reset threshold to base value via zero volatility update
        # AdaptiveThresholdComponent doesn't have explicit reset, but we can
        # reset threshold via update_threshold(0.0) which returns to base
        self._adaptive_threshold_component.update_threshold(0.0)

        # Component 4: PerformanceMonitoringComponent doesn't have reset method
        # Counters are not resettable by design (cumulative metrics)

        # Reset signal state
        self._last_signal_bar = -self._signal_config.min_signal_separation_bars
        self._last_close_price = None
        self.log.info("Signal generation state reset (facade)")

    def get_signal_statistics(self) -> dict[str, Any]:
        """
        Aggregate statistics from all components.

        Returns
        -------
        dict[str, Any]
            Dictionary with:
            - signal_count: Total signals generated
            - error_count: Total errors encountered
            - avg_latency_ms: Average prediction latency
            - p99_latency_ms: P99 prediction latency
            - current_threshold: Adaptive threshold
            - current_regime: Market regime
            - buffer_utilization: Ring buffer fill percentage
            - bars_processed: Total bars processed

        """
        perf_stats = self._performance_monitoring_component.get_current_stats()
        buffer_metadata = self._prediction_buffer_component.get_ring_metadata()

        capacity = self._prediction_buffer_component.capacity
        count = buffer_metadata.get("_prediction_ring_count", 0)

        return {
            "signal_count": perf_stats.get("signal_count", 0),
            "error_count": perf_stats.get("error_count", 0),
            "avg_latency_ms": perf_stats.get("avg_total_ms", 0.0),
            "p99_latency_ms": perf_stats.get("p99_total_ms", 0.0),
            "current_threshold": self._adaptive_threshold_component.current_threshold,
            "current_regime": self._adaptive_threshold_component.current_regime,
            "buffer_utilization": count / max(capacity, 1),
            "bars_processed": self._bars_processed,
            "prediction_count": self._prediction_count,
        }

